// Imprime el objeto Math, que proporciona funciones y constantes matemáticas
console.log(Math); // Resultado: (el objeto Math)

// Imprime el valor de la constante PI
console.log(Math.PI); // Resultado: 3.141592653589793

// Retorna el valor absoluto de un número
console.log(Math.abs(-7.8)); // Resultado: 7.8

// Redondea hacia arriba al número entero más cercano
console.log(Math.ceil(7.2)); // Resultado: 8

// Redondea hacia abajo al número entero más cercano
console.log(Math.floor(7.8)); // Resultado: 7

// Redondea al número entero más cercano
console.log(Math.round(7.49)); // Resultado: 7

// Calcula la raíz cuadrada de un número
console.log(Math.sqrt(81)); // Resultado: 9

// Calcula una base elevada a la potencia de un exponente
console.log(Math.pow(2, 5)); // Resultado: 32

// Retorna el signo de un número (-1 si es negativo, 0 si es cero, 1 si es positivo)
console.log(Math.sign(-7.8)); // Resultado: -1

// Retorna un número pseudo-aleatorio entre 0 (inclusive) y 1 (exclusivo)
console.log(Math.random()); // Resultado: un número aleatorio entre 0 y 1

// Retorna un número pseudo-aleatorio entre 0 (inclusive) y 1000 (exclusivo)
console.log(Math.random() * 1000); // Resultado: un número aleatorio entre 0 y 1000

// Retorna un número entero pseudo-aleatorio entre 0 (inclusive) y 1000 (exclusivo)
console.log(Math.round(Math.random() * 1000)); // Resultado: un número entero aleatorio entre 0 y 1000
